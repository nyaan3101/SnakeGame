//Funcion de casos posible para cambiar la direccion de movimiento de la serpiente
    private void formKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyPressed
        switch (evt.getKeyCode()) {
            case KeyEvent.VK_LEFT:
                panel.cambiodireccion("iz");
                break;
            case KeyEvent.VK_RIGHT:
                panel.cambiodireccion("de");
                break;
            case KeyEvent.VK_DOWN:
                panel.cambiodireccion("ar");
                break;
            case KeyEvent.VK_UP:
                panel.cambiodireccion("ab");
                break;
            default:
                break;
        }
    }//GEN-LAST:event_formKeyPressed

